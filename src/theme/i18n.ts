const DICTIONARY: Record<Locale, Dict> = { en: EN, it: IT };

export const translate = (locale: Locale, key: string): string =>
  DICTIONARY[locale]?.[key] ?? DICTIONARY.en[key] ?? key;

/** Hook that re-renders when the user switches language. */
export const useT = () => {
  const locale = useSettingsStore((s) => s.settings.language);
  return (key: string) => translate(locale, key);
};
